# Contoh penggunaan kondisi elif
hari_ini = "Minggu"
if (hari_ini == "Senin"):
    print("Saya akan kuliah, mata kuliah Anda di hari senin!")
elif (hari_ini == "Selasa"):
    print("Saya akan kuliah, mata kuliah Anda di hari selasa!")
elif (hari_ini == "Rabu"):
    print("Saya akan kuliah, mata kuliah Anda di hari rabu!")
elif (hari_ini == "Kamis"):
    print("Saya akan kuliah, mata kuliah Anda di hari kamis!")
elif (hari_ini == "Jumat"):
    print("Saya akan kuliah, mata kuliah Anda di hari jumat!")
elif (hari_ini == "Sabtu"):
    print("Saya Libur Kuliah, dan mengerjakan tugas-tugas kuliah!")
elif (hari_ini == "Minggu"):
    print("Saya Libur Kuliah!")
