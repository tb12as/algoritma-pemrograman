# list mencetak semua nilai yang ada di dalamnya
motto_polnep = ['Tepat', 'Waktu', 'Tepat', 'Aturan', 'Tepat', 'Ukuran']
print('Cetak list motto_polnep :', motto_polnep)

# set akan mencetak nilai yang sama sekali saja
# simple way : set remove all the duplicates
motto_polnep = {'Tepat', 'Waktu', 'Tepat', 'Aturan', 'Tepat', 'Ukuran'}
print('Cetak set motto_polnep :', motto_polnep)

# mengubah list menjadi set dengan fungsi set()
motto_polnep = set(['Tepat', 'Waktu', 'Tepat', 'Aturan', 'Tepat', 'Ukuran'])
print('Cetak list diubah menjadi set :', motto_polnep)


