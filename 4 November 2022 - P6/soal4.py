# menghitung luas & keliling persegi panjang
panjang = float(input('Masukkan panjang persegi panjang : '))
lebar = float(input('Masukkan lebar persegi panjang : '))
keliling_persegi_panjang = 2 * (panjang + lebar)
luas_persegi_panjang = panjang * lebar

print(f'Sebuah persegi panjang memiliki panjang {panjang} cm, lebar {lebar} cm, dan memiliki luas sebesar {luas_persegi_panjang} cm^2, dan keliling {keliling_persegi_panjang} cm')
